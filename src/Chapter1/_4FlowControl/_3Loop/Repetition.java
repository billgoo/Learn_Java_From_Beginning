package Chapter1._4FlowControl._3Loop;

public class Repetition {
    public static void main(String[] args) {
        int arr[] = {7, 10, 1};
        System.out.println("元素为：");
        for (int x: arr) {
            System.out.println(x);
        }
        //元素为：
        //7
        //10
        //1
    }
}
